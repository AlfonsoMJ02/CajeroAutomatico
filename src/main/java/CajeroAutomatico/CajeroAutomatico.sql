CREATE OR REPLACE PROCEDURE BancoGetAllSP(
    pCursor OUT SYS_REFCURSOR
)
AS
BEGIN
    OPEN pCursor FOR SELECT IdBanco, Nombre FROM Banco ORDER BY IdBanco;
END;
