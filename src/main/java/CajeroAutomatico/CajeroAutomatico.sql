CREATE TABLE Banco (
    IdBanco NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre VARCHAR(100) NOT NULL UNIQUE
)

CREATE TABLE Usuario (
    IdUsuario NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Nombre VARCHAR(50) NOT NULL,
    ApellidoPaterno VARCHAR(50) NOT NULL,
    ApellidoMaterno VARCHAR(50) NOT NULL,
    Curp VARCHAR(18) NOT NULL UNIQUE,
    FechaNacimiento DATE NOT NULL,
    Email VARCHAR(100) NOT NULL UNIQUE,
    Password VARCHAR(200) NOT NULL,
    telefono VARCHAR(15) NOT NULL,
    FechaCreacion DATE DEFAULT SYSDATE
)

CREATE TABLE Cuenta(
    IdCuenta NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdUsuario NUMBER,
    IdBanco NUMBER,
    NumeroCuenta VARCHAR(20) UNIQUE,
    Saldo NUMBER (12,2),
    
    FOREIGN KEY(IdUsuario) REFERENCES Usuario(IdUsuario),
    FOREIGN KEY(IdBanco) REFERENCES Banco(IdBanco)
) 

CREATE TABLE Tarjeta(
    IdTarjeta NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IdCuenta NUMBER,
    NumeroTarjeta VARCHAR(20) UNIQUE,
    Nip NUMBER(4),
    VerificacionNip NUMBER(1) DEFAULT 0,
    
    FOREIGN KEY(IdCuenta) REFERENCES Cuenta(IdCuenta)
)


